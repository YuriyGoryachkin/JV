package com.company;

public class for_while {
    public static void main(String[] args){
        for(int i=0;i<10;i++){
            System.out.println("i = " + i);
        }
        int i = 0;
        do {
            System.out.println("i = " + i);
            i++;
        }
        while (i<10);
        int i2 = 0;
        while (i2<10){
            System.out.println("i = " + i2);
            i2++;
        }

    }
}
